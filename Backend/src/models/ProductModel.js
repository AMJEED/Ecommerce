const mongoose = require('mongoose');

const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  imageURL: String // URL to store the image
});

const ProductModel = mongoose.model('Product', productSchema);

module.exports = ProductModel;
